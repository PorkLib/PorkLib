package net.daporkchop.lib.hash;

import lombok.Getter;
import lombok.NonNull;
import net.daporkchop.lib.hash.helper.md.MD2Helper;
import net.daporkchop.lib.hash.helper.md.MD4Helper;
import net.daporkchop.lib.hash.helper.md.MD5Helper;
import net.daporkchop.lib.hash.helper.ripemd.RipeMD128Helper;
import net.daporkchop.lib.hash.helper.ripemd.RipeMD160Helper;
import net.daporkchop.lib.hash.helper.sha.Sha160Helper;
import net.daporkchop.lib.hash.helper.sha.Sha256Helper;
import net.daporkchop.lib.hash.helper.sha.Sha384Helper;
import net.daporkchop.lib.hash.helper.sha.Sha512Helper;

import java.util.function.Function;

/**
 * A list of all supported hashing algorithms
 *
 * @author DaPorkchop_
 */
public enum HashAlg {
    MD2(MD2Helper::md2, MD2Helper::md2, 16),
    MD4(MD4Helper::md4, MD4Helper::md4, 16),
    MD5(MD5Helper::md5, MD5Helper::md5, 16),
    RIPEMD_128(RipeMD128Helper::ripeMD128, RipeMD128Helper::ripeMD128, 16),
    RIPEMD_160(RipeMD160Helper::ripeMD160, RipeMD160Helper::ripeMD160, 20),
    SHA_160(Sha160Helper::sha160, Sha160Helper::sha160, 20),
    SHA_256(Sha256Helper::sha256, Sha256Helper::sha256, 32),
    SHA_384(Sha384Helper::sha384, Sha384Helper::sha384, 48),
    SHA_512(Sha512Helper::sha512, Sha512Helper::sha512, 64);

    private final Function<byte[], byte[]> hashFunction;
    private final Function<byte[][], byte[]> multiHashFunction;

    /**
     * The size (in bytes) of a hash generated by this algorithm
     */
    @Getter
    private final int length;

    HashAlg(Function<byte[], byte[]> hashFunction, Function<byte[][], byte[]> multiHashFunction, int length) {
        this.hashFunction = hashFunction;
        this.multiHashFunction = multiHashFunction;
        this.length = length;
    }

    public byte[] hash(@NonNull byte[] toHash) {
        return this.hashFunction.apply(toHash);
    }

    public byte[] hash(byte[]... toHash) {
        for (byte[] b : toHash) {
            if (b == null) {
                throw new NullPointerException();
            }
        }
        return this.multiHashFunction.apply(toHash);
    }
}
